export function getBookAbbr(name: string): string {
  for (const key in BIBLE_BOOKS) {
    const [fullName, shortName, englishName, englishShort] = BIBLE_BOOKS[key];
    if (
      name === fullName ||
      name === shortName ||
      name === englishName ||
      name === englishShort
    ) {
      return key;
    }
  }
  return "Nul";
}

export function getBookIndex(key: string): number | null {
  let index = 0;
  for (const bookKey in BIBLE_BOOKS) {
    if (bookKey === key) {
      return index;
    }
    index++;
  }
  return null;
}

export function validateVerseNum(
  book: string,
  chapter: number,
  verse: number
): boolean {
  if (verse > 0) return BIBLE_BOOKS[book][chapter - 1].length >= verse;
  return false;
}
